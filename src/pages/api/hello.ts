////////////////////////////////////////////////
// Imports:
////////////////////////////////////////////////
import type { NextApiRequest, NextApiResponse } from 'next';

////////////////////////////////////////////////
// Types and Interfaces:
////////////////////////////////////////////////
type ResponseData = {
  name: string;
};

////////////////////////////////////////////////
// Constants:
////////////////////////////////////////////////
const DEFAULT_NAME = 'John Doe';

////////////////////////////////////////////////
// Enums:
////////////////////////////////////////////////
// No enums needed for this handler

////////////////////////////////////////////////
// Helper Functions:
////////////////////////////////////////////////
// No helper functions needed for this handler

////////////////////////////////////////////////
// Handler Function:
////////////////////////////////////////////////
function handler(
  req: NextApiRequest,
  res: NextApiResponse<ResponseData>
) {
  res.status(200).json({ name: DEFAULT_NAME });
}

////////////////////////////////////////////////
// Export:
////////////////////////////////////////////////
export default handler;
